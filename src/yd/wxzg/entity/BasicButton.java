package yd.wxzg.entity;

/**
 * 按钮基类
 */

public class BasicButton {
    private String name;
    private String url;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
    
}
